	$formMAMEScreensaverConfi_Load = {
		$cfg = Get-ini
		$textboxINIPATH.Text = $inifile
		$textboxMamePath.Text = $cfg.mamepath
		$textboxROMPath.Text = $cfg.rompath
		$textboxROMListPath.Text = $cfg.romlistpath
		$textboxConfigPath.Text = $cfg.configpath
		$textboxsnapshotpath.Text = $cfg.snapshotpath
		$textboxnvrampath.Text = $cfg.nvrampath
		$Numericruntime.Value = $cfg.runtime
		$numericVolume.Value = $cfg.volume
		
		# Get current timeout value
		[string]$regtimeout = & REG query "HKCU\Control Panel\Desktop" /v ScreenSaveTimeout | Out-Null
		$regtimeout | foreach {
			if ($_ -match "/d{1,32000}$")
			{
				[int]$sstimeout = $Matches[0]			
				break
			}
		}
		$numericsstimeout.Value = $sstimeout
	}
	
	$buttonSave_Click = {
		[string]$newconfig = "mamepath=$($textboxMamePath.Text)`r`n"
		$newconfig += "rompath=$($textboxROMPath.Text)`r`n"
		$newconfig += "romlistpath=$($textboxromlistpath.Text)`r`n"
		$newconfig += "configpath=$($textboxConfigPath.Text)`r`n"
		$newconfig += "snapshotpath=$($textboxsnapshotpath.Text)`r`n"
		$newconfig += "nvrampath=$($textboxnvrampath.Text)`r`n"
		$newconfig += "runtime=$($Numericruntime.Value)`r`n"
		$newconfig += "volume=$($numericVolume.Value)`r`n"
		$newconfig += "mamearguments=$($textboxMameArguments.Text)`r`n"
		try
		{
			Set-Content -Path $inifile -Value $newconfig
		}
		catch
		{
			if ($Error[0] -match "is denied")
			{
				$statusbar.Text = "Access denied saving .ini file.  Launch MameSSCfg.exe as administrator."
			}		
		}
	}
	
