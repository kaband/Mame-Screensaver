		$textboxnvrampath.Text = $cfg.nvrampath
		$Numericruntime.Value = $cfg.runtime
		$numericVolume.Value = $cfg.volume
		
		# Get current timeout value - if registry entry exists	
		$psjob = Start-Job -ScriptBlock { & REG query "HKCU\Control Panel\Desktop" }
		
		do
		{
			sleep -Milliseconds 10
		}
		until ($psjob.State -eq "Completed")
		$reglist = Receive-Job $psjob
	
		#$reglist = Invoke-Process "reg" -ArgumentList 'query "HKCU\Control Panel\Desktop"'
		
		if ($reglist -match "ScreenSaveTimeout")
		{
			[string]$regtimeout = & REG query "HKCU\Control Panel\Desktop" /v ScreenSaveTimeout
			$regtimeout | foreach-object {
				if ($_ -match "/d{1,32000}$")
				{
					[int]$sstimeout = $Matches[0]
					break
				}
			}
		}
		$numericsstimeout.Value = $sstimeout
		
		$fileVersion = [System.Diagnostics.FileVersionInfo]::GetVersionInfo("$mydir\mamesscfg.exe")
		$textboxVersion.Text = $fileVersion.FileVersion
	}
	
