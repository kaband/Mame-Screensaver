		$statusbar.Text = "Generating Rom List File ..."
		$statusbar.Update()
		$roms = Get-ChildItem -Path $textboxrompath.text -Filter *.zip -File | select Name, Extension
		New-Item -Name $textboxrompath.text -ItemType File
		foreach ($rom in $roms)
		{
			$romname = $rom.name -replace $rom.extension, ""
			Add-Content -path $textboxromlistpath.text -Value "$romname"
		}
