	$buttonGenerateRomList_Click = {
		$statusbar.Text = "Generating Rom List File ..."
		$statusbar.Update()
		$roms = Get-ChildItem -Path $rompath -Filter *.zip -File | select Name, Extension
		Set-Content romlist.txt -value ""
		foreach ($rom in $roms)
		{
			$rom.name -replace $rom.extension,"" | Add-Content -path romlist.txt -Value "$_"
		}
		$statusbar.Text = "Generating Rom List File ... done."
	}
	
	
